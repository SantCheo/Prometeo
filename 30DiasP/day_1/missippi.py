import time  # Importa el módulo time para usar la función sleep

# Bucle for que cuenta hasta cinco
for i in range(1, 6):  # range(1, 6) genera números del 1 al 5
    print(i, "Mississippi")  # Imprime el número y la palabra "Mississippi"
    time.sleep(1)  # Pausa la ejecución durante 1 segundo

# Mensaje final
print("¡Listos o no, ahí voy!")
